biomarker_table = pd.read_csv("candidate_biomarker_table.csv")

candidate_genes = biomarker_table["gene_symbol"].tolist()
print("Candidate biomarker genes for classifier:", candidate_genes)
full_deg_table = pd.read_csv("GSE19804_full_DEG_table_annotated.csv")
candidate_probes = full_deg_table[full_deg_table["gene_symbol"].isin(candidate_genes)][["probe_id", "gene_symbol"]].drop_duplicates(subset="gene_symbol")

X = expr.loc[candidate_probes["probe_id"]].T
X.columns = candidate_probes["gene_symbol"].values
X = X.merge(metadata[["sample_id", "condition", "pair_id"]], left_index=True, right_on="sample_id")

y = (X["condition"] == "Tumor").astype(int)
print("Feature matrix shape:", X.shape)
print(X[["sample_id", "condition", "pair_id"] + candidate_genes[:3]].head())
unique_patients = X["pair_id"].unique()
train_patients, test_patients = train_test_split(unique_patients, test_size=0.25, random_state=42)

train_mask = X["pair_id"].isin(train_patients)
test_mask = X["pair_id"].isin(test_patients)

X_train = X.loc[train_mask, candidate_genes]
X_test = X.loc[test_mask, candidate_genes]
y_train = y.loc[train_mask]
y_test = y.loc[test_mask]

print("Train patients:", len(train_patients), "-> samples:", X_train.shape[0])
print("Test patients:", len(test_patients), "-> samples:", X_test.shape[0])
print("Train class balance:\n", y_train.value_counts())
print("Test class balance:\n", y_test.value_counts())
overlap = set(train_patients) & set(test_patients)
print("Patient overlap between train/test (should be empty set):", overlap)
scaler = StandardScaler()
X_train_scaled = scaler.fit_transform(X_train)
X_test_scaled = scaler.transform(X_test)
clf = LogisticRegression(max_iter=1000, random_state=42)
clf.fit(X_train_scaled, y_train)

y_pred = clf.predict(X_test_scaled)
