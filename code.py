meta_tumor = metadata[metadata["condition"] == "Tumor"].sort_values("pair_id")
meta_normal = metadata[metadata["condition"] == "Normal"].sort_values("pair_id")
assert list(meta_tumor["pair_id"]) == list(meta_normal["pair_id"]), \
    "Pair IDs do not align between Tumor and Normal — STOP, fix before continuing"

tumor_ids = meta_tumor["sample_id"].tolist()
normal_ids = meta_normal["sample_id"].tolist()

tumor_df = expr[tumor_ids]
normal_df = expr[normal_ids]

print("Tumor samples:", tumor_df.shape)
print("Normal samples:", normal_df.shape)
print("Pair alignment confirmed for", len(tumor_ids), "patients")
log2fc = tumor_df.mean(axis=1) - normal_df.mean(axis=1)

t_stats, p_values = stats.ttest_rel(tumor_df.values, normal_df.values, axis=1)

deg_table = pd.DataFrame({
    "probe_id": expr.index,
    "log2FC": log2fc.values,
    "t_statistic": t_stats,
    "p_value": p_values
})

print("DEG table shape:", deg_table.shape)
deg_table.head()
reject, fdr_values, _, _ = multipletests(deg_table["p_value"], method="fdr_bh")
deg_table["FDR"] = fdr_values

# Check for any NaN p-values (can happen if a probe has zero variance)
n_nan = deg_table["p_value"].isna().sum()
print("Probes with NaN p-value (zero variance, will be excluded from significant set):", n_nan)
def get_direction(row):
    if row["FDR"] < 0.05 and row["log2FC"] > 1:
        return "Up"
    elif row["FDR"] < 0.05 and row["log2FC"] < -1:
        return "Down"
    else:
        return "Not Significant"

deg_table["direction"] = deg_table.apply(get_direction, axis=1)

print(deg_table["direction"].value_counts())
full_deg_table = deg_table.sort_values("FDR")
significant_deg_table = full_deg_table[full_deg_table["direction"] != "Not Significant"].copy()

