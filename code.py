from sklearn.model_selection import GroupKFold

groups_train = X.loc[train_mask, "pair_id"]
gkf = GroupKFold(n_splits=5)

cv_aucs = []
for fold, (tr_idx, val_idx) in enumerate(gkf.split(X_train, y_train, groups=groups_train)):
    X_tr, X_val = X_train.iloc[tr_idx], X_train.iloc[val_idx]
    y_tr, y_val = y_train.iloc[tr_idx], y_train.iloc[val_idx]

    fold_scaler = StandardScaler()
    X_tr_scaled = fold_scaler.fit_transform(X_tr)
    X_val_scaled = fold_scaler.transform(X_val)

    fold_clf = LogisticRegression(max_iter=1000, random_state=42)
    fold_clf.fit(X_tr_scaled, y_tr)
    fold_auc = roc_auc_score(y_val, fold_clf.predict_proba(X_val_scaled)[:, 1])
    cv_aucs.append(fold_auc)
    print(f"Fold {fold+1} AUC: {fold_auc:.3f}")

print(f"\nMean CV AUC: {np.mean(cv_aucs):.3f} (+/- {np.std(cv_aucs):.3f})")
results_summary = pd.DataFrame({
    "metric": ["Test AUC", "Sensitivity", "Specificity", "Precision", "F1", "Mean CV AUC", "CV AUC Std"],
    "value": [auc, sensitivity, specificity, precision, f1, np.mean(cv_aucs), np.std(cv_aucs)]
})
results_summary.to_csv("classifier_performance_summary.csv", index=False)
print(results_summary)