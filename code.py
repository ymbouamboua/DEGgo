def parse_condition(title):
    if "Cancer" in title or title.strip().endswith("T"):
        return "Tumor"
    elif "Normal" in title or title.strip().endswith("N"):
        return "Normal"
    else:
        return "Unknown"

def parse_pair_id(title):
    match = re.search(r'(\d+)[TN]?\s*$', title.strip())
    return match.group(1) if match else None

